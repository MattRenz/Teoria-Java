public class ciclo_while {
    public static void main(String[] args) {

        // Il while scorre attraverso il codice finchè una condizione specificata è vera

        /*
         * while (condizione){
         * // codice da eseguire
         * }
         */

        int i = 0;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }
    }
}
